
<?php
    // vòng lặp for
    for($i = 1; $i<=5; $i++){
        echo "<br>hello PHP";
    }

    //vòng lặp while
    $dem = 1;
    while($dem <=5){
        echo "<h3>$dem. Hello world.<h3>";
        $dem++;
    }

    // Vòng lặp do...While
    $dem = 1;
    do{
        echo "<h3>$dem .Vòng lặp do ... While</h3>";
        $dem++;
    }while($dem<=5);
?>